
public class Animal {
    public Animal(String espece, String name, int age, boolean vaccin, String action){
        this.mEspece = espece;
        this.mName = name;
        this.mAge = age;
        this.mVaccin = vaccin;
        this.mAction = action;
    }

    public String mEspece;
    public String mName;
    public String mAge;
    public String mVaccin;
    public String mAction;
}